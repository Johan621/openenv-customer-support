@app.websocket("/ws/{session_id}")
async def websocket_endpoint(websocket: WebSocket, session_id: str) -> None:
    """
    WebSocket endpoint for low-latency agent interaction.

    Message format (JSON):
      {"type": "reset", "difficulty": "easy", "seed": null}
      {"type": "step", "action": {<TriageAction fields>}}
      {"type": "state"}
    """
    await websocket.accept()
    env = _get_or_create_env(session_id)
    logger.info("WebSocket connected: session=%s", session_id)

    try:
        while True:
            raw = await websocket.receive_text()
            try:
                msg: dict[str, Any] = json.loads(raw)
            except json.JSONDecodeError:
                await websocket.send_text(
                    json.dumps({"error": "Invalid JSON"})
                )
                continue

            msg_type = msg.get("type")
            try:
                if msg_type == "reset":
                    difficulty = msg.get("difficulty", "easy")
                    seed = msg.get("seed")
                    obs = env.reset(difficulty=difficulty, seed=seed)
                    await websocket.send_text(obs.model_dump_json())

                elif msg_type == "step":
                    from models import TriageAction
                    action_data = msg.get("action", {})
                    action = TriageAction(**action_data)
                    obs = env.step(action)
                    await websocket.send_text(obs.model_dump_json())

                elif msg_type == "state":
                    state = env.state()
                    await websocket.send_text(state.model_dump_json())

                else:
                    await websocket.send_text(
                        json.dumps({"error": f"Unknown message type: {msg_type!r}"})
                    )
            except (ValueError, RuntimeError) as exc:
                await websocket.send_text(json.dumps({"error": str(exc)}))

    except WebSocketDisconnect:
        logger.info("WebSocket disconnected: session=%s", session_id)
        _sessions.pop(session_id, None)


