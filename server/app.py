# ---------------------------------------------------------------------------
# Session registry
# ---------------------------------------------------------------------------

_sessions: dict[str, CustomerSupportEnv] = {}


def _get_or_create_env(session_id: str) -> CustomerSupportEnv:
    if session_id not in _sessions:
        env = CustomerSupportEnv()
        env.session_id = session_id
        _sessions[session_id] = env
    return _sessions[session_id]


# ---------------------------------------------------------------------------
# Default global environment (for single-client usage)
# ---------------------------------------------------------------------------

_default_env = None

def get_default_env():
    global _default_env
    if _default_env is None:
        logger.info("Initializing default environment...")
        _default_env = CustomerSupportEnv()
        logger.info("Environment initialized successfully!")
    return _default_env

# ---------------------------------------------------------------------------
# Lifespan
# ---------------------------------------------------------------------------

@asynccontextmanager
async def lifespan(app: FastAPI):
    logger.info("Customer Support Triage environment starting up...")
    # Don't initialize here - lazy load on first request
    yield
    logger.info("Shutting down environment server.")


# ---------------------------------------------------------------------------
# App
# ---------------------------------------------------------------------------

app = FastAPI(
    title="Customer Support Triage RL Environment",
    description=(
        "Real-world customer support ticket triage environment for the "
        "Meta-PyTorch-HuggingFace OpenEnv Hackathon Round 1. "
        "The agent learns to classify, prioritize, and route support tickets."
    ),
    version="0.1.0",
    lifespan=lifespan,
)


# ---------------------------------------------------------------------------
# HTTP Endpoints
# ---------------------------------------------------------------------------

@app.get("/health", tags=["System"])
def health_check() -> dict[str, str]:
    """Health check endpoint for Docker and load balancers."""
    return {"status": "ok", "environment": "customer-support-triage", "version": "0.1.0"}


@app.post("/reset", response_model=TriageObservation, tags=["Environment"])
def reset_episode(request: ResetRequest) -> TriageObservation:
    """
    Start a new episode.

    - **difficulty**: `easy` | `medium` | `hard`
    - **seed**: optional integer for reproducibility
    """
    try:
        obs = get_default_env().reset(difficulty=request.difficulty, seed=request.seed)
        return obs
    except ValueError as exc:
        raise HTTPException(status_code=400, detail=str(exc)) from exc


@app.post("/step", response_model=TriageObservation, tags=["Environment"])
def step_environment(request: StepRequest) -> TriageObservation:
    """
    Submit a triage action for the current ticket.

    Returns the next observation, reward, and done flag.
    """
    try:
        obs = get_default_env().step(request.action)
        return obs
    except RuntimeError as exc:
        raise HTTPException(status_code=400, detail=str(exc)) from exc


@app.get("/state", response_model=EnvironmentState, tags=["Environment"])
def get_state() -> EnvironmentState:
    """Get a snapshot of the current environment state."""
    return get_default_env().state()


